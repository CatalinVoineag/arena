namespace Map {
  data init() {
    SDL_GetWindowSize(window, &windowWidth, &windowHight);
    data map = {
      .widthTiles = windowWidth / 64,
      .hightTiles = (windowHight / 64) + 1,
    };

    return map;
  }

  void update() {
    for (int w = 0; w < mapArray[0].size(); w++) {
      for (int h = 0; h < mapArray.size(); h++) {
        SDL_FRect rect;
        rect.x = 0 + (64 * w);
        rect.y = 0 + (64 * h);
        rect.w = 64;
        rect.h = 64;

        SDL_FRect subRect;
        subRect.x = mapCoordinates[mapArray[h][w]].first;
        subRect.y = mapCoordinates[mapArray[h][w]].second;
        subRect.w = 64;
        subRect.h = 64;

        SDL_RenderTexture(renderer, tileMapTexture, &subRect, &rect);
      }
    }
  }
}

